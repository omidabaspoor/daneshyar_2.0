    private static function normText($s) {
        $s = mb_strtolower((string)$s, 'UTF-8');
        $s = str_replace(['ي','ك','ۀ','ة','ؤ','إ','أ','آ'], ['ی','ک','ه','ه','و','ا','ا','ا'], $s);
        $s = str_replace(["\xE2\x80\x8C", "\xE2\x80\x8D"], ' ', $s); // ZWNJ/ZWJ
        $s = preg_replace('/\s+/u', ' ', $s);
        return trim($s);
    }

    private static function containsAny($hay, array $words) {
        foreach ($words as $w) {
            $w = self::normText($w);
            if ($w !== '' && mb_strpos($hay, $w, 0, 'UTF-8') !== false) return true;
        }
        return false;
    }

